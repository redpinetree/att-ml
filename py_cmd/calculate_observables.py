	def accel_asc(n):
		a=[0 for i in range(n + 1)]
		k=1
		y=n-1
		while k!=0:
			x=a[k-1]+1
			k-=1
			while 2*x<=y:
				a[k]=x
				y-=x
				k+=1
			l=k+1
			while x<=y:
				a[k]=x
				a[l]=y
				yield a[:k+2]
				x+=1
				y-=1
			a[k]=x+y
			y=x+y-1
			yield a[:k+1]
			
	def sum_coef(c,n):
		res=1
		for i in c:
			res*=spspec.binom(n,i)
			n-=i
		return res
	
